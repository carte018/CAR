package edu.internet2.consent.car;

public class AddableItem {
	private String rpdisp;
	private String rhdisp;
	private String rhtype;
	private String rhid;
	private String rptype;
	private String rpid;
	private String usertype;
	
	public String getUsertype() {
		return usertype;
	}
	public void setUsertype(String usertype) {
		this.usertype = usertype;
	}
	public String getRpdisp() {
		return rpdisp;
	}
	public void setRpdisp(String rpdisp) {
		this.rpdisp = rpdisp;
	}
	public String getRhdisp() {
		return rhdisp;
	}
	public void setRhdisp(String rhdisp) {
		this.rhdisp = rhdisp;
	}
	public String getRhtype() {
		return rhtype;
	}
	public void setRhtype(String rhtype) {
		this.rhtype = rhtype;
	}
	public String getRhid() {
		return rhid;
	}
	public void setRhid(String rhid) {
		this.rhid = rhid;
	}
	public String getRptype() {
		return rptype;
	}
	public void setRptype(String rptype) {
		this.rptype = rptype;
	}
	public String getRpid() {
		return rpid;
	}
	public void setRpid(String rpid) {
		this.rpid = rpid;
	}
	
	
	
}
